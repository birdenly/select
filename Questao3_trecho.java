public class Questao3_trecho {
    public static void main(String[] args) {
        int INDICE = 12, SOMA = 0, K = 1;
        while (K < INDICE) {
            K = K + 1;
            SOMA = SOMA + K;
        }
        System.out.println("##############################");
        System.out.println("Valor da variavel SOMA: "+SOMA);
        System.out.println("##############################");
    }
}
